public class bai1 {
	public static void main(String[] args) {
		float a = 4.0f, b = 16.0f;
		System.out.println("Gia tri tuyet doi: " + Math.abs(a) + " " + Math.abs(b));
		System.out.println("Ca 2 cua a va b la: " + Math.sqrt(a) + " " + Math.sqrt(b));
		System.out.println("Max a va b: " + Math.max(a, b));
		System.out.println("Min a va b: " + Math.min(a, b));
		System.out.println("Mu 3 cua a va b la: " + Math.pow(a, 3) + " " + Math.pow(b, 3));
	}
}